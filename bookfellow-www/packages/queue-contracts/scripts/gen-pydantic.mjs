#!/usr/bin/env node
/**
 * Generate worker/src/generated/jobs.py from TS queue contracts (SSOT).
 * Do not hand-edit the generated Python file.
 */
import { mkdirSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const outPath = join(here, "../../../worker/src/generated/jobs.py");

const body = `# AUTO-GENERATED by packages/queue-contracts (pnpm gen:queue). DO NOT EDIT.

from __future__ import annotations

from typing import Literal, Union

from pydantic import BaseModel, Field

QUEUE_NAME = "bookfellow-jobs"


class PackBuildJob(BaseModel):
    type: Literal["pack.build"] = "pack.build"
    job_id: str = Field(alias="jobId")
    book_id: str = Field(alias="bookId")
    requested_at: str = Field(alias="requestedAt")

    model_config = {"populate_by_name": True}


class CreditScanJob(BaseModel):
    type: Literal["credit.scan"] = "credit.scan"
    requested_at: str = Field(alias="requestedAt")

    model_config = {"populate_by_name": True}


JobPayload = Union[PackBuildJob, CreditScanJob]
`;

mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(outPath, body, "utf8");
console.log(`wrote ${outPath}`);
